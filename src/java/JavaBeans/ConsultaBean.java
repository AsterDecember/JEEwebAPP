package JavaBeans;

/**
 *
 * @author Lenovo
 */
public class ConsultaBean {
    private int NRC;
    private int ID_profesor;
    private String NombreP;
    private String ApellidoP;
    private int ID_materia;
    private String NombreM;

    /**
     * @return the NRC
     */
    public int getNRC() {
        return NRC;
    }

    /**
     * @param NRC the NRC to set
     */
    public void setNRC(int NRC) {
        this.NRC = NRC;
    }

    /**
     * @return the ID_profesor
     */
    public int getID_profesor() {
        return ID_profesor;
    }

    /**
     * @param ID_profesor the ID_profesor to set
     */
    public void setID_profesor(int ID_profesor) {
        this.ID_profesor = ID_profesor;
    }

    /**
     * @return the NombreP
     */
    public String getNombreP() {
        return NombreP;
    }

    /**
     * @param NombreP the NombreP to set
     */
    public void setNombreP(String NombreP) {
        this.NombreP = NombreP;
    }

    /**
     * @return the ApellidoP
     */
    public String getApellidoP() {
        return ApellidoP;
    }

    /**
     * @param ApellidoP the ApellidoP to set
     */
    public void setApellidoP(String ApellidoP) {
        this.ApellidoP = ApellidoP;
    }

    /**
     * @return the ID_materia
     */
    public int getID_materia() {
        return ID_materia;
    }

    /**
     * @param ID_materia the ID_materia to set
     */
    public void setID_materia(int ID_materia) {
        this.ID_materia = ID_materia;
    }

    /**
     * @return the NombreM
     */
    public String getNombreM() {
        return NombreM;
    }

    /**
     * @param NombreM the NombreM to set
     */
    public void setNombreM(String NombreM) {
        this.NombreM = NombreM;
    }
    
    
    
}
